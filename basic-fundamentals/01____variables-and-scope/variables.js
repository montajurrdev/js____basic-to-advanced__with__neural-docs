// a variable is a container for storing data

// let (can be reassigned)
let age = 25;
age = 23;

console.log(age); // 23

//
// const (cannot be reassigned)
const country = "Bangladesh";
// country = "india";  Error

console.log(country); // Bangladesh

//
// var (older, function-scoped)
var name = "Montajur";
console.log(name);

